#!/usr/bin/env bash
set -euo pipefail

# ============================================================
# AMRFinderPlus (nucleotide input)
# ============================================================

# ------------------------------------------------------------
# Tool path (edit as needed)
# ------------------------------------------------------------
AMRFINDER_BIN="/path/to/amrfinder"

# ------------------------------------------------------------
# Input / output
# ------------------------------------------------------------
INPUT_FASTA="/path/to/input_sequences.fasta"
OUT_TXT="/path/to/amrfinder_output.txt"

# ------------------------------------------------------------
# Prepare output directory
# ------------------------------------------------------------
mkdir -p "$(dirname "$OUT_TXT")"

# ------------------------------------------------------------
# Run
# ------------------------------------------------------------
"$AMRFINDER_BIN" --nucleotide "$INPUT_FASTA" > "$OUT_TXT"
